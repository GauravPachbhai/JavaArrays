
/*
Perfect cube and perfect Square
*/
class PerfectcubeS{
	public static void main(String args[]){
	
		int n= 4;
			
		if(Math.cbrt(n)%1 == 0)
			System.out.println(n+" is a perfect cube");
		if(Math.sqrt(n)%1 == 0)
			System.out.println(n+" is a perfect square");
		
		
	}
}

